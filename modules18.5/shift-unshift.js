// array shift 
// shift means Removes the first element from an array and returns it
let myShift = [100,200,300,400,500];
myShift.shift();
console.log(myShift);




//array unshift
// unshift means Inserts new elements at the start of an array
let arrayUnshift = [45,46,47,48,49,44];
arrayUnshift.unshift(500);
console.log(arrayUnshift);
